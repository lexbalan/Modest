
#include <stddef.h>
#include <stdint.h>
#include <stdbool.h>
#include <string.h>
#include <stdio.h>
#include <stdlib.h>
#include "./crc32.h"

#ifndef LENGTHOF
#define LENGTHOF(x) (sizeof(x) / sizeof((x)[0]))
#endif /* LENGTHOF */


#define DATA_BUFFER_LENGTH  128


struct test {
	uint8_t data[DATA_BUFFER_LENGTH];
	uint32_t len;
	uint32_t hash;
};

static struct test tests[3] = {
	{.data = {0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39}, .len = 9, .hash = 0xCBF43926L},
	{.data = {0x54, 0x68, 0x65, 0x20, 0x71, 0x75, 0x69, 0x63, 0x6B, 0x20, 0x62, 0x72, 0x6F, 0x77, 0x6E, 0x20, 0x66, 0x6F, 0x78, 0x20, 0x6A, 0x75, 0x6D, 0x70, 0x73, 0x20, 0x6F, 0x76, 0x65, 0x72, 0x20, 0x74, 0x68, 0x65, 0x20, 0x6C, 0x61, 0x7A, 0x79, 0x20, 0x64, 0x6F, 0x67}, .len = 43, .hash = 0x414FA339},
	{.data = {0x54, 0x65, 0x73, 0x74, 0x20, 0x76, 0x65, 0x63, 0x74, 0x6F, 0x72, 0x20, 0x66, 0x72, 0x6F, 0x6D, 0x20, 0x66, 0x65, 0x62, 0x6F, 0x6F, 0x74, 0x69, 0x2E, 0x63, 0x6F, 0x6D}, .len = 28, .hash = 0xC877F61}
};

static bool runTest(struct test *test) {
	const uint32_t crc = crc32_run((uint8_t (*)[])&test->data, test->len);
	return crc == test->hash;
}


int main(void) {
	printf("test CRC32\n");

	bool success = true;

	uint32_t i = 0;
	while (i < (uint32_t)LENGTHOF(tests)) {
		if (!runTest(&tests[i])) {
			printf("test #%d failed\n", i);
			success = false;
		} else {
			printf("test #%d passed\n", i);
		}
		i = i + 1;
	}

	printf("test ");
	if (!success) {
		printf("failed\n");
		return EXIT_FAILURE;
	}

	printf("passed\n");
	return EXIT_SUCCESS;
}


