__attribute__((always_inline))
static inline uint8_t rj_sbox(uint8_t x) {
	return ((const uint8_t [256])SBOX)[x];
}

__attribute__((always_inline))
static inline uint8_t rj_sboxInv(uint8_t x) {
	return ((const uint8_t [256])SBOXINV)[x];
}

static void subBytes(aes256_Block *block) {
	uint8_t i = 0;
	while (i < 16) {
		(*block)[i] = rj_sbox((*block)[i]);
		i = i + 1;
	}
}

static void subBytesInv(aes256_Block *block) {
	uint8_t i = 0;
	while (i < 16) {
		(*block)[i] = rj_sboxInv((*block)[i]);
		i = i + 1;
	}
}

static void addRoundKey(aes256_Block *block, uint8_t (*k)[16]) {
	uint8_t i = 0;
	while (i < 16) {
		(*block)[i] = (*block)[i] ^ (*k)[i];
		i = i + 1;
	}
}

static void addRoundKeyCpy(aes256_Block *block, aes256_Key *key, aes256_Key *cpk) {
	uint8_t i = 0;
	while (i < 16) {
		const uint8_t yy = (*key)[i];
		(*cpk)[i] = yy;
		(*block)[i] = (*block)[i] ^ yy;
		(*cpk)[16 + i] = (*key)[16 + i];
		i = i + 1;
	}
}

static void shiftRows(aes256_Block *block) {
	uint8_t i;
	uint8_t j;
	i = (*block)[1];
	(*block)[1] = (*block)[5];
	(*block)[5] = (*block)[9];
	(*block)[9] = (*block)[13];
	(*block)[13] = i;
	i = (*block)[10];
	(*block)[10] = (*block)[2];
	(*block)[2] = i;
	j = (*block)[3];
	(*block)[3] = (*block)[15];
	(*block)[15] = (*block)[11];
	(*block)[11] = (*block)[7];
	(*block)[7] = j;
	j = (*block)[14];
	(*block)[14] = (*block)[6];
	(*block)[6] = j;
}

