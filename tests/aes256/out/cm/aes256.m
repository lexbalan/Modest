@inline
func rj_sbox (x: Word8) -> Byte {
	return sbox[Nat8 x]
}



@inline
func rj_sboxInv (x: Word8) -> Byte {
	return sboxinv[Nat8 x]
}


func subBytes (block: *Block) -> {} {
	var i = Nat8 0
	while i < lengthof(Block) {
		block[i] = rj_sbox(block[i])
		i = i + 1
	}
}


func subBytesInv (block: *Block) -> {} {
	var i = Nat8 0
	while i < lengthof(Block) {
		block[i] = rj_sboxInv(block[i])
		i = i + 1
	}
}


func addRoundKey (block: *Block, k: *[16]Byte) -> {} {
	var i = Nat8 0
	while i < lengthof(Block) {
		block[i] = block[i] ^ k[i]
		i = i + 1
	}
}


func addRoundKeyCpy (block: *Block, key: *Key, cpk: *Key) -> {} {
	var i = Nat8 0
	while i < lengthof(Block) {
		let yy: Byte = key[i]
		cpk[i] = yy
		block[i] = block[i] ^ yy
		cpk[16 + i] = key[16 + i]
		i = i + 1
	}
}


func shiftRows (block: *Block) -> {} {
	var i: Word8
	var j: Word8

	i = block[1]
	block[1] = block[5]
	block[5] = block[9]
	block[9] = block[13]
	block[13] = i

	i = block[10]
	block[10] = block[2]
	block[2] = i

	j = block[3]
	block[3] = block[15]
	block[15] = block[11]
	block[11] = block[7]
	block[7] = j

	j = block[14]
	block[14] = block[6]
	block[6] = j
}


func shiftRowsInv (block: *Block) -> {} {
	var i: Word8
	var j: Word8

	i = block[1]
	block[1] = block[13]
	block[13] = block[9]
	block[9] = block[5]
	block[5] = i

	i = block[2]
	block[2] = block[10]
	block[10] = i

	j = block[3]
	block[3] = block[7]
	block[7] = block[11]
	block[11] = block[15]
	block[15] = j

	j = block[6]
	block[6] = block[14]
	block[14] = j
}


func mixColumns (block: *Block) -> {} {
	var a: Word8
	var b: Word8
	var c: Word8
	var d: Word8
	var e: Word8

	var i: Nat8 = 0
	while i < lengthof(Block) {
		a = block[i + 0]
		b = block[i + 1]
		c = block[i + 2]
		d = block[i + 3]
		e = a ^ b ^ c ^ d
		block[i + 0] = block[i + 0] ^ e ^ rj_xtime(a ^ b)
		block[i + 1] = block[i + 1] ^ e ^ rj_xtime(b ^ c)
		block[i + 2] = block[i + 2] ^ e ^ rj_xtime(c ^ d)
		block[i + 3] = block[i + 3] ^ e ^ rj_xtime(d ^ a)
		i = i + 4
	}
}


func mixColumnsInv (block: *Block) -> {} {
	var a: Word8
	var b: Word8
	var c: Word8
	var d: Word8
	var e: Word8
	var x: Word8
	var y: Word8
	var z: Word8

	var i: Nat8 = 0
	while i < lengthof(Block) {
		a = block[i + 0]
		b = block[i + 1]
		c = block[i + 2]
		d = block[i + 3]
		e = a ^ b ^ c ^ d
		z = rj_xtime(e)
		x = e ^ rj_xtime(rj_xtime(z ^ a ^ c))
		y = e ^ rj_xtime(rj_xtime(z ^ b ^ d))
		block[i + 0] = block[i + 0] ^ x ^ rj_xtime(a ^ b)
		block[i + 1] = block[i + 1] ^ y ^ rj_xtime(b ^ c)
		block[i + 2] = block[i + 2] ^ x ^ rj_xtime(c ^ d)
		block[i + 3] = block[i + 3] ^ y ^ rj_xtime(d ^ a)
		i = i + 4
	}
}


func expandEncKey (k: *Key, rc: *Byte) -> {} {
	var i: Nat8

	k[0] = k[0] ^ rj_sbox(k[29]) ^ *rc
	k[1] = k[1] ^ rj_sbox(k[30])
	k[2] = k[2] ^ rj_sbox(k[31])
	k[3] = k[3] ^ rj_sbox(k[28])
	*rc = rj_xtime(*rc)

	i = 4
	while i < 16 {
		k[i + 0] = k[i + 0] ^ k[i - 4]
		k[i + 1] = k[i + 1] ^ k[i - 3]
		k[i + 2] = k[i + 2] ^ k[i - 2]
		k[i + 3] = k[i + 3] ^ k[i - 1]
		i = i + 4
	}

	k[16] = k[16] ^ rj_sbox(k[12])
	k[17] = k[17] ^ rj_sbox(k[13])
	k[18] = k[18] ^ rj_sbox(k[14])
	k[19] = k[19] ^ rj_sbox(k[15])

	i = 20
	while i < 32 {
		k[i + 0] = k[i + 0] ^ k[i - 4]
		k[i + 1] = k[i + 1] ^ k[i - 3]
		k[i + 2] = k[i + 2] ^ k[i - 2]
		k[i + 3] = k[i + 3] ^ k[i - 1]
		i = i + 4
	}
}


func expandDecKey (k: *Key, rc: *Byte) -> {} {
	var i: Nat8

	i = 28
	while i > 16 {
		k[i + 0] = k[i + 0] ^ k[i - 4]
		k[i + 1] = k[i + 1] ^ k[i - 3]
		k[i + 2] = k[i + 2] ^ k[i - 2]
		k[i + 3] = k[i + 3] ^ k[i - 1]
		i = i - 4
	}

	k[16] = k[16] ^ rj_sbox(k[12])
	k[17] = k[17] ^ rj_sbox(k[13])
	k[18] = k[18] ^ rj_sbox(k[14])
	k[19] = k[19] ^ rj_sbox(k[15])

	i = 12
	while i > 0 {
		k[i + 0] = k[i + 0] ^ k[i - 4]
		k[i + 1] = k[i + 1] ^ k[i - 3]
		k[i + 2] = k[i + 2] ^ k[i - 2]
		k[i + 3] = k[i + 3] ^ k[i - 1]
		i = i - 4
	}

	var y: Word8 = 0
	if *rc & 1 != 0 {
		y = 0x8D
	}

	*rc = (*rc >> 1) ^ y

	k[0] = k[0] ^ rj_sbox(k[29]) ^ *rc
	k[1] = k[1] ^ rj_sbox(k[30])
	k[2] = k[2] ^ rj_sbox(k[31])
	k[3] = k[3] ^ rj_sbox(k[28])
}


public func init (ctx: *Context, key: *Key) -> Result {
	if ctx == nil or key == nil {
		return resultError
	}

	ctx.deckey = *key
	ctx.enckey = *key

	var rcon: Byte = 1
	var i: Nat8 = 0
	while i < 7 {
		expandEncKey(&ctx.deckey, &rcon)
		i = i + 1
	}

	return resultSuccess
}


public func encrypt_ecb (ctx: *Context, block: *Block) -> Result {
	if ctx == nil or block == nil {
		return resultError
	}

	var rcon: Byte = 1
	addRoundKeyCpy(block, &ctx.enckey, &ctx.key)

	var i: Nat8 = 0
	while i < 13 {
		i = i + 1
		subBytes(block)
		shiftRows(block)
		mixColumns(block)
		if Word8 i & 1 == 1 {
			addRoundKey(block, &ctx.key[16:32])
		} else {
			expandEncKey(&ctx.key, &rcon)
			addRoundKey(block, &ctx.key[0:16])
		}
	}

	subBytes(block)
	shiftRows(block)
	expandEncKey(&ctx.key, &rcon)
	addRoundKey(block, &ctx.key[0:16])

	return resultSuccess
}


public func decrypt_ecb (ctx: *Context, block: *Block) -> Result {
	if ctx == nil or block == nil {
		return resultError
	}

	addRoundKeyCpy(block, &ctx.deckey, &ctx.key)
	shiftRowsInv(block)
	subBytesInv(block)

	var rcon: Byte = 0x80
	var i: Nat8 = 13
	while i > 0 {
		if Word8 i & 1 == 1 {
			expandDecKey(&ctx.key, &rcon)
			addRoundKey(block, &ctx.key[16:32])
		} else {
			addRoundKey(block, &ctx.key[0:16])
		}

		mixColumnsInv(block)
		shiftRowsInv(block)
		subBytesInv(block)
		i = i - 1
	}

	addRoundKey(block, &ctx.key[0:16])

	return resultSuccess
}


public func deinit (ctx: *Context) -> Result {
	if ctx == nil {
		return resultError
	}
	var zeroKey = Key []
	ctx.key = zeroKey
	ctx.enckey = zeroKey
	ctx.deckey = zeroKey

	return resultSuccess
}

