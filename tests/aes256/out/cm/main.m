func runTest (test: *TestCase) -> Bool {
	var ctx: Context

	aes.init(&ctx, &test.key)

	var ptBefore: Block = test.pt

	aes.encrypt_ecb(&ctx, &test.pt)

	if test.pt != test.ct {
		printf("FAILED (encrypt)")
		return false
	}

	aes.decrypt_ecb(&ctx, &test.pt)

	if test.pt != ptBefore {
		printf("FAILED (decrypt)")
		return false
	}

	aes.deinit(&ctx)

	printf("PASSED")
	return true
}


public func main () -> Int32 {
	printf("run AES-256 test\n")

	var success: Bool = true

	var i: Nat8 = 0
	while i < lengthof(tests) {
		printf("run test #%d ", i)
		let rc: Bool = runTest(&tests[i])
		success = success and rc
		printf("\n")
		i = i + 1
	}

	printf("AES-256 test ")
	if not success {
		printf("failed\n")
		return exitFailure
	}

	printf("passed\n")
	return exitSuccess
}

